package sample;

public class For {
    public static void main(String[] args) {
        for (var i = 1; i <= 10; i++) {
            System.out.println(i);
}
    }
    {
        for (int i = 2; i <= 100; i+=2) {
            if (i > 20) {
                // 繰り返しから抜け出す
                break;
            }
            System.out.println(i);            
        }}}
    
    
    

